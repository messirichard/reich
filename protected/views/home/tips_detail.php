                    <h3>5 tips efektif untuk saniter yang efektif dan efisien</h3>
                    <div class="clear height-20"></div>
                    <p>14 Simple Gardening Tips and Tricks<br />
                    From using leftover coffee beans to preventing dirt from getting underneath fingernails, master gardener Paul James shares his top 14 tips and shortcuts to make spring gardening a breeze.</p>

                    <p>Assorted Flowerpots<br />
                    Little clay pots make great cloches for protecting young plants from overnight frosts.<br />
                    Here, the latest tips and tricks from Paul James, host of Gardening by the Yard:</p>

                    <p>1. To remove the salt deposits that form on clay pots, combine equal parts white vinegar, rubbing alcohol and water in a spray bottle. Apply the mixture to the pot and scrub with a plastic brush. Let the pot dry before you plant anything in it.</p>

                    <p>2. To prevent accumulating dirt under your fingernails while you work in the garden, draw your fingernails across a bar of soap and you&#39;ll effectively seal the undersides of your nails so dirt can&#39;t collect beneath them. Then, after you&#39;ve finished in the garden, use a nailbrush to remove the soap and your nails will be sparkling clean.</p>

                    <p>3. To prevent the line on your string trimmer from jamming or breaking, treat with a spray vegetable oil before installing it in the trimmer.</p>

                    <p>4. Turn a long-handled tool into a measuring stick! Lay a long-handled garden tool on the ground, and next to it place a tape measure. Using a permanent marker, write inch and foot marks on the handle. When you need to space plants a certain distance apart (from just an inch to several feet) you&#39;ll already have a measuring device in your hand.</p>

                    <p>5. To have garden twine handy when you need it, just stick a ball of twine in a small clay pot, pull the end of the twine through the drainage hole, and set the pot upside down in the garden. Do that, and you&#39;ll never go looking for twine again.</p>

                    <p>6. Little clay pots make great cloches for protecting young plants from sudden, overnight frosts and freezes.</p>

                    <p>7. To turn a clay pot into a hose guide, just stab a roughly one-foot length of steel reinforcing bar into the ground at the corner of a bed and slip two clay pots over it: one facing down, the other facing up. The guides will prevent damage to your plants as you drag the hose along the bed.</p>

                    <p>8. To create perfectly natural markers, write the names of plants (using a permanent marker) on the flat faces of stones of various sizes and place them at or near the base of your plants.</p>

                    <p>9. Got aphids? You can control them with a strong blast of water from the hose or with insecticidal soap. But here&#39;s another suggestion, one that&#39;s a lot more fun; get some tape! Wrap a wide strip of tape around your hand, sticky side out, and pat the leaves of plants infested with aphids. Concentrate on the undersides of leaves, because that&#39;s where the little buggers like to hide.</p>

                    <p>10. The next time you boil or steam vegetables, don&#39;t pour the water down the drain, use it to water potted patio plants, and you&#39;ll be amazed at how the plants respond to the &quot;vegetable soup.&quot;</p>

                    <p>11. Use leftover tea and coffee grounds to acidify the soil of acid-loving plants such as azaleas, rhododendrons, camellias, gardenias and even blueberries. A light sprinkling of about one-quarter of an inch applied once a month will keep the pH of the soil on the acidic side.</p>

                    <p>12. Use chamomile tea to control damping-off fungus, which often attacks young seedlings quite suddenly. Just add a spot of tea to the soil around the base of seedlings once a week or use it as a foliar spray.</p>

                    <p>13. If you need an instant table for tea service, look no farther than your collection of clay pots and saucers. Just flip a good-sized pot over, and top it off with a large saucer. And when you&#39;ve had your share of tea, fill the saucer with water, and your &quot;table&quot; is now a birdbath.</p>

                    <p>14. The quickest way in the world to dry herbs: just lay a sheet of newspaper on the seat of your car, arrange the herbs in a single layer, then roll up the windows and close the doors. Your herbs will be quickly dried to perfection. What&#39;s more, your car will smell great.</p>
